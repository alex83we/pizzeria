    /**
     * Store a newly created resource in storage.
     *
     * @param Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $folderPath = public_path('images/profile_picture/');

        $image_parts = explode(";base64,", $request->image);
        $image_type_aux = explode("image/", $image_parts[0]);
        $image_type = $image_type_aux[1];
        $image_base64 = base64_decode($image_parts[1]);

        $imageName = str_replace(' ', '_', auth()->user()->name) . '.png';

        $imageFullPath = $folderPath.$imageName;

        file_put_contents($imageFullPath, $image_base64);

        $user = auth()->user()->id;
        DB::table('users')->where('id', '=', $user)->update(['photo' => $imageName, 'updated_at' => now()]);

        return response()->json(['success'=> trans('Crop Image Uploaded Successfully')]);
    }

